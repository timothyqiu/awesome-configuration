-- Simple function to load additional LUA files.
local function loadrc(name)
    local success
    local result

    -- Which file?
    local path = awful.util.getdir("config") .. "/rc/" .. name .. ".lua"

    success, result = pcall(function () return dofile(path) end)
    if not success then
        naughty.notify({
            title  = "Error while loading an RC file",
            text   = "When loading `" .. name .. "`, got the following error:\n" .. result,
            preset = naughty.config.presets.critical
        })
    end

    return result
end

